
create table form(
    userid int,
    FirstName varchar(25),
    LastName varchar(25),
    EmailAddress varchar(30),
    countryCode varchar(5),
    PhoneNumber varchar(10),
    DateOfBirth date,
    Gender varchar(10),
    Country varchar(25),
    localaddress varchar(50),
    City varchar(25),
    PostalCode varchar(10),
    Primary Key (userid)
);